package com.shenma.yueba.yangjia.modle;

public class TaskListItem {

	
	private String id;
	private String icom;//小图标
	private String name;//奖励名称
	private String status;
	private String amount;
	private String surplusamount;
	private String tip;//进度
	private String link;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIcom() {
		return icom;
	}
	public void setIcom(String icom) {
		this.icom = icom;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getSurplusamount() {
		return surplusamount;
	}
	public void setSurplusamount(String surplusamount) {
		this.surplusamount = surplusamount;
	}
	public String getTip() {
		return tip;
	}
	public void setTip(String tip) {
		this.tip = tip;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	
	
	
	
}
